-- Tambah partisi penjualan untuk tahun 2031-2040 + default catch-all.
-- Mencegah INSERT failure jika tanggal jatuh di luar partisi yang terdaftar.
-- Tabel `penjualan_item` TIDAK di-partition (PK = id), jadi tidak perlu dimirror.

CREATE TABLE IF NOT EXISTS penjualan_2031 PARTITION OF penjualan FOR VALUES FROM ('2031-01-01') TO ('2032-01-01');
CREATE TABLE IF NOT EXISTS penjualan_2032 PARTITION OF penjualan FOR VALUES FROM ('2032-01-01') TO ('2033-01-01');
CREATE TABLE IF NOT EXISTS penjualan_2033 PARTITION OF penjualan FOR VALUES FROM ('2033-01-01') TO ('2034-01-01');
CREATE TABLE IF NOT EXISTS penjualan_2034 PARTITION OF penjualan FOR VALUES FROM ('2034-01-01') TO ('2035-01-01');
CREATE TABLE IF NOT EXISTS penjualan_2035 PARTITION OF penjualan FOR VALUES FROM ('2035-01-01') TO ('2036-01-01');
CREATE TABLE IF NOT EXISTS penjualan_2036 PARTITION OF penjualan FOR VALUES FROM ('2036-01-01') TO ('2037-01-01');
CREATE TABLE IF NOT EXISTS penjualan_2037 PARTITION OF penjualan FOR VALUES FROM ('2037-01-01') TO ('2038-01-01');
CREATE TABLE IF NOT EXISTS penjualan_2038 PARTITION OF penjualan FOR VALUES FROM ('2038-01-01') TO ('2039-01-01');
CREATE TABLE IF NOT EXISTS penjualan_2039 PARTITION OF penjualan FOR VALUES FROM ('2039-01-01') TO ('2040-01-01');
CREATE TABLE IF NOT EXISTS penjualan_2040 PARTITION OF penjualan FOR VALUES FROM ('2040-01-01') TO ('2041-01-01');
CREATE TABLE IF NOT EXISTS penjualan_default PARTITION OF penjualan DEFAULT;
